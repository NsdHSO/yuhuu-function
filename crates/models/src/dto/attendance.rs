use sea_orm::entity::prelude::*;
use serde::{Deserialize, Serialize};

#[derive(Clone, Debug, PartialEq, DeriveEntityModel, Serialize, Deserialize)]
#[sea_orm(schema_name = "church", table_name = "attendances")]
pub struct Model {
    #[sea_orm(primary_key, auto_increment = true)]
    pub id: i64,

    #[sea_orm(unique)]
    pub uuid: Uuid,

    pub user_id: i64,
    pub service_type: String, // Sunday Service, Midweek, Cell Group, Prayer Meeting, etc.
    pub attendance_date: Date,
    pub check_in_time: Option<DateTime>,
    pub check_out_time: Option<DateTime>,
    pub status: String, // Present, Absent, Late, Excused
    pub notes: Option<String>,
    pub recorded_by: Option<i64>, // Who recorded this attendance
    pub created_at: DateTime,
    pub updated_at: DateTime,
}

#[derive(Copy, Clone, Debug, EnumIter, DeriveRelation)]
pub enum Relation {
    #[sea_orm(
        belongs_to = "super::user::Entity",
        from = "Column::UserId",
        to = "super::user::Column::Id"
    )]
    User,
}

impl Related<super::user::Entity> for Entity {
    fn to() -> RelationDef {
        Relation::User.def()
    }
}

impl ActiveModelBehavior for ActiveModel {}
